                                    как можно лучше отложился у меня в памяти, потому как не знаю, когда еще смогу побывать
                                    возле кратера действующего вулкана.</p>
                                <h2>Заключение</h2>
                                <p>Спускаться было также непросто, особенно в самом начале по сыпучим мелким камням, и торопиться
                                    здесь не стоит, иначе можно упасть и пораниться. В джунглях было немного проще, но тоже
                                    время от времени приходилось прилагать определенные усилия чтобы не навернуться, к тому
                                    же пошел небольшой дождь и тропа быстро размокала. Однако спустя три часа я добрался
                                    до остановки, где уставший, но очень довольный поехал обратно в отель.</p>
                                <p>Однодневный трек на вулкан
                                    <strong>Марапи</strong> в одиночестве вполне осуществим. Общее время на это приключение может
                                    составить от 7-8 часов в зависимости от физической формы. У меня подъем занял 4 часа,
                                    спуск — 3 часа и один час я провел на самой вершине, плюс время на дорогу от отеля до
                                    поворота на вулкан. Никаких денег за подъем я не платил, единственно только за проезд
                                    на маршрутках (16 000 рупий или примерно 1,2 доллара, туда-обратно). По сути бесплатное
                                    путешествие, а впечатлений на миллион.</p>
