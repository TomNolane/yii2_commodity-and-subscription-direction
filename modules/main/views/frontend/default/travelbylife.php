                            просто незабываемый вид на окружающую местность. Находясь в там, ощущаешь себя маленькой песчинкой
                            в этом огромном и прекрасном мире, которой все по плечу.</p>
                        <h2>На вершине</h2>
                        <p>Взобравшись наверх моему взору предстал совершенно другой мир. Мне почему-то вспомнилась
