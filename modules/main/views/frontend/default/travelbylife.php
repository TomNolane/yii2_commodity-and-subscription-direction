                        вполне возможно, однако стоит иметь достаточно хорошую физическую форму. Общее время подъема и спуска
                        может занять весь день, и лучше всего начинать с самого раннего утра. Я отправился на гору около
                        8 утра, а вернулся в свой отель в начале седьмого вечера, когда уже начиналось смеркаться.</p>
                    <p>Никаких особых проблем для подъема в одиночку нет, хотя в отеле местные гиды меня активно заверяли, что
