                <div class="entry-content">
                    <p>Одно из
                        <a title="Новогодние желания путешественника" href="http://travelbylife.ru/novogodnie-zhelaniya-puteshestvennika/"
                            target="_blank" rel="noopener">моих новогодних желаний</a> было посетить Индонезию и подняться на какой-нибудь из вулканов, которых
                        предостаточно в этой чудесной стране. И всего несколько месяцев спустя мое желание реализовалось.
                        Мне посчастливилось взобраться на вершину действующего вулкана
                        <strong>Марапи</strong> на острове Суматра, потрогать вулканический песок, почувствовать характерный запах
                        серы и заглянуть в кратер этого чуда природы.</p>
                    <p>
                        <span id="more-1668">
                            <br> 
                            <br>
                        </span>
                    </p>
                    <p>Так как мне очень хотелось посмотреть красоту острова Суматра собственными глазами, то после
                        <a title="Озеро Тоба на Суматре в Индонезии"
                            href="http://travelbylife.ru/ozero-toba-na-sumatre-v-indonezii/" target="_blank" rel="noopener">нескольких замечательных дней на озере Тоба</a>, мой путь лежал в город Букиттинги, вблизи которого
                        есть сразу два вулкана
                        <strong>Марапи</strong> и Синггалан. Благодаря подробной информации
                        <em>Ивана «Tanaw» Лешукова</em> и его
                        <a title="Блог странствующего натуралиста" href="http://travel-to-parks.ru/"
                            target="_blank" rel="noopener">блога странствующего натуралиста</a>, я решил посетить один из них, тем более, на это могло бы хватить
                        всего одного дня.</p>
                    <p>Конечно, подъем на это чудо природы с палаткой, более предпочтителен, так как на вершине просто потрясающей
                        красоты виды, да и восход солнца, тоже, рассказывают, очень запоминающийся. Однако если время поездки
                        ограниченно, то в один день уложиться можно, что и было проверенно на себе.</p>
                    <figure id="attachment_1675"
                        style="width: 800px" class="wp-caption aligncenter">
