                        <h2>Что взять с собой</h2>
                        <p>Отправляясь в такой однодневный трек непременно захватите с собой:</p>
                        <ul>
                            <li>как минимум пару литров воды;</li>
                            <li>сухой паек;</li>
                            <li>запасную футболку;</li>
                            <li>маленькое полотенце;</li>
                            <li>солнцезащитный крем;</li>
                            <li>дождевик;</li>
                            <li>лейкопластырь или бинт с каким-нибудь антисептиком.</li>
                        </ul>
                        <p>Ну и конечно, стоит заранее побеспокоиться о хороших походных кроссовках.</p>
                        <h2>По дороге на вершину</h2>
                        <p>Но вернемся обратно к однодневному треку на вулкан. Постепенно дорога становиться грунтовой и уходит
                            в заросли джунглей, где совсем приходится подниматься уже по земле вспученной длинными и проворными
                            корнями высоких деревьев.&nbsp; Дорога на вулкан по большей части пролегает среди джунглей. Тропа
                            хорошо натоптана и заблудиться довольно сложно, просто порой она довольно круто поднимается и
                            приходиться прилагать определенные усилия чтобы двигаться вперед.</p>
