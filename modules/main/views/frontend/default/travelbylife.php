                        <h2>Как добраться</h2>
                        <p>Чтобы взобраться на вулкан
                            <strong>Марапи</strong> стоит остановиться в городе
                            <em>Букиттинги</em>, откуда до места подъема всего 20 минут езды на обычной местной маршрутке. Из
                            центра города сначала на красной маршрутке за 3000 рупий доезжаем до района
                            <em>Симпанг Райя</em>, где пересаживаемся на другую уже голубую маршрутку, которая едет до
                            <em>Кота Бару</em> и просим водителя, чтобы остановил у поворота на вулкан, проезд стоит 5000 рупий.</p>
                        <h2>Стоимость восхождения</h2>
                        <p>По сути, с этого поворота и начинается постепенный подъем на саму вершину, сначала по бетонной дороге
                            доходим до деревянного домика, больше напоминающий сарай, где необходимо отметиться и записать
                            свое имя в журнал. Здесь с меня потребовали еще заплатить 20 000 рупий за восхождение, на что
                            я ответил отказом, так как посчитал такую цену очень дорогой. Ничего не заплатив я пошел дальше
                            на скрывающийся в облаках вулкан, никто меня не стал останавливать. Поэтому не стоит вестись,
                            если с вас будут требовать денег за восхождение. Позже я встретил местных туристов, которые сообщили
                            мне что подъем на
                            <strong>Марапи</strong> совершенно бесплатен.</p>
