                                <em>«Марсианские хроники»</em>
                            </a>, словно я оказался на совсем другой планете, где все иначе. Пейзаж на вершине вулкана
                            <strong>Марапи</strong>, конечно запоминается: парящие огромные облака, несколько больших кратеров из
                            которых клубиться пар, необычайного цвета камни и несильный, но настойчивый запах серы.</p>
