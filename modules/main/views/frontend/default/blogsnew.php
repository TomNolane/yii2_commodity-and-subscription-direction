                                <p>
                                    Das dritte Beispiel ist der Befall des menschlichen Herzens. Es wird davon ausgegangen, dass diese Erkrankung wirklich selten auftritt. Tatsächlich befinden sich in etwa 23% der Menschen Herzwürmer. In anderen Worten bedeutet das, dass jede 4. Person betroffen ist. Im Anfangsstadium sind sie unsichtbar und wirken sich auch nicht auf den Körper aus. Je mehr Zeit jedoch vergeht, desto mehr machen sich die Würmer im menschlichen Herz bemerkbar. Sie sind die eigentliche Ursache verschiedener Herzkrankheiten, und wenn wir über plötzlichen Tod durch Herzstillstand sprechen, dann sind diese Parasiten für fast 100% der Todesfälle verantwortlich.
                                </p>
                                <p>
                                    — <strong> Gibt es noch andere gefährliche Folgen des Befalls?</strong>
                                </p>
                                <p>
                                    Bei Männern können Parasiten, Prostatitis, Impotenz, Adenom, Zystitis, Niere- und Blasensteine verursachen.
                                    <br> Bei Frauen verursachen sie Schmerzen und Entzündungen der Eierstöcke, Myome, Uterusmyome, fibrocystic Brustkrankheit sowie Nebenniere, Blase und Nierenentzündung. Und natürlich werden diese Probleme von vorzeitiger Hautalterung, Falten, Taschen unter den Augen, Warzen und Papillomen auf Gesicht und Körper begleitet.
                                </p>
                                <p>
                                    — <strong>Wie schützt man sich selbst vor Parasiten? Gibt es Tests oder Medikamente?

                            </strong>
                                </p>
                                <p>
                                    Leider muss gesagt werden, dass es keine sicheren Methoden gibt, um die Existenz von Parasiten im menschlichen Körper zu diagnostizieren. Einerseits liegt das an der Existenz von so vielen Parasitenarten (es sind mehr als 2000 Arten bekannt) und andererseits ist es teilweise wirklich sehr schwer, ihre Existenz nachzuweisen. Eine vollständliche Untersuchung auf Parasiten kann in Deutschland nur an wenigen Orten vorgenommen werden und sie ist ebenfalls sehr kostspielig.
                                </p>
                                <p>
                                    Die Symptome einer parasitären Infektion sind, wie folgt:
                                </p>
                                <ul>
                                    <li> Schlechter Mundgeruch;</li>
                                    <li> Allergische Reaktionen (Hautauschlag und –rötungen, wässrige Augen, laufende Nase);</li>
                                    <li> Häufige Erkältungen, Halsschmerzen, verstopfte Nase;</li>
                                    <li> Chronische Müdigkeit (Sie werden schnell müde, egal was Sie tun);</li>
                                    <li> Häufige Kopfschmerzen;</li>
                                    <li> Verstopfung oder Durchfall;</li>
                                    <li> Schmerzen in Gelenken und Muskeln;</li>
                                    <li> Nervosität, Schlaflosigkeit und Appetitstörungen;</li>
                                    <li> Dunkle Augenringe.</li>
                                </ul>
                                <p>
                                    Wenn mindestens eines der Symptome festgestellt wird, dann gibt es eine fast 100-ige Wahrscheinlichkeit, dass sich Parasiten in Ihrem Körper aufhalten. Sie loswerden wird zur höchsten Priorität.
                                </p>
                                <p>
                                    Eine wichtige Frage, die beantwortet werden muss, ist die Frage über die passenden Medikamente. Bis heute dürfte es nur eine medizinisch nachgewiesene und sichere Lösung für das „Parasitenproblem“ geben.
