<p>Наверняка многим из вас следующая ситуация покажется знакомой: вы занялись собой и привели свое тело в порядок - похудели, подкачали ягодицы и стали выглядеть почти как с обложки журнала. Но затем времени на спорт вдруг стало не хватать, питание нарушилось, и вы с ужасом осознаете, что набираете сброшенные с огромным трудом килограммы обратно…</p>



